import React from 'react';
function Bubble() {
  return (
    <div>
      <p>{`do`}</p>

      <p>{`swapped = false`}</p>

      <p>{`for i = 1 to indexOfLastUnsortedElement-1`}</p>

      <p>{`if leftElement > rightElement`}</p>

      <p>{`swap(leftElement, rightElement)`}</p>

      <p>{`swapped = true; ++swapCounter`}</p>

      <p>{`while swapped`}</p>
    </div>
  );
}
export default Bubble;
